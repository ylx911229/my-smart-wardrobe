import React, { useState, useEffect } from 'react';
import {
  View,
  StyleSheet,
  ScrollView,
  Alert,
  Image,
  TouchableOpacity,
  ActivityIndicator,
} from 'react-native';
import {
  TextInput,
  Button,
  Card,
  Title,
  Menu,
  Text,
  Chip,
  ProgressBar
} from 'react-native-paper';
import { Ionicons } from '@expo/vector-icons';
import * as ImagePicker from 'expo-image-picker';

import { theme } from '../styles/theme';
import { useDatabase } from '../services/DatabaseContext';
import { analyzeClothingImage, generateDefaultTags } from '../services/ClothingAnalysisService';
import type { StackNavigationProp } from '@react-navigation/stack';
import type { WardrobeStackParamList, Category, ClothingTags } from '../types';

type AddClothingScreenNavigationProp = StackNavigationProp<
  WardrobeStackParamList,
  'AddClothing'
>;

interface AddClothingScreenProps {
  navigation: AddClothingScreenNavigationProp;
}

interface FormData {
  name: string;
  photo_uri: string;
  brand: string;
  color: string;
  size: string;
  location: string;
  notes: string;
}

const AddClothingScreen = ({ navigation }: AddClothingScreenProps) => {
  const { addClothing } = useDatabase();
  
  const [loading, setLoading] = useState(false);
  const [analyzing, setAnalyzing] = useState(false);
  const [analysisProgress, setAnalysisProgress] = useState(0);
  const [categories, setCategories] = useState<Category[]>([]);
  const [selectedCategory, setSelectedCategory] = useState<Category | null>(null);
  const [categoryMenuVisible, setCategoryMenuVisible] = useState(false);
  const [smartTags, setSmartTags] = useState<ClothingTags | null>(null);
  
  const [formData, setFormData] = useState<FormData>({
    name: '',
    photo_uri: '',
    brand: '',
    color: '',
    size: '',
    location: '',
    notes: ''
  });

  useEffect(() => {
    loadCategories();
    requestPermissions();
  }, []);

  const requestPermissions = async () => {
    // ËØ∑Ê±ÇÁõ∏ÂÜåÊùÉÈôê
    const mediaLibraryStatus = await ImagePicker.requestMediaLibraryPermissionsAsync();
    if (mediaLibraryStatus.status !== 'granted') {
      Alert.alert('ÊùÉÈôêÈúÄÊ±Ç', 'ÈúÄË¶ÅËÆøÈóÆÁõ∏ÂÜåÁöÑÊùÉÈôêÊù•ÈÄâÊã©ÁÖßÁâá');
    }
    
    // ËØ∑Ê±ÇÁõ∏Êú∫ÊùÉÈôê
    const cameraStatus = await ImagePicker.requestCameraPermissionsAsync();
    if (cameraStatus.status !== 'granted') {
      Alert.alert('ÊùÉÈôêÈúÄÊ±Ç', 'ÈúÄË¶ÅËÆøÈóÆÁõ∏Êú∫ÁöÑÊùÉÈôêÊù•ÊãçÊëÑÁÖßÁâá');
    }
  };

  const loadCategories = async () => {
    try {
      // ‰ΩøÁî®È¢ÑÂÆö‰πâÁöÑÂàÜÁ±ªÂàóË°®
      const categoriesData: Category[] = [
        { id: '1', name: '‰∏äË°£', color: '#FF6B6B' },
        { id: '2', name: '‰∏ãË£Ö', color: '#4ECDC4' },
        { id: '3', name: 'Â§ñÂ•ó', color: '#45B7D1' },
        { id: '4', name: 'ÈûãÂ≠ê', color: '#96CEB4' },
        { id: '5', name: 'ÈÖçÈ•∞', color: '#FFEAA7' },
        { id: '6', name: 'ÂÖ∂‰ªñ', color: '#DDA0DD' }
      ];
      setCategories(categoriesData);
    } catch (error) {
      console.error('Error loading categories:', error);
    }
  };

  const handleInputChange = (field: keyof FormData, value: string) => {
    setFormData(prev => ({ ...prev, [field]: value }));
  };

  const handleCategorySelect = (category: Category) => {
    setSelectedCategory(category);
    setCategoryMenuVisible(false);
  };

  const handleImagePicker = () => {
    Alert.alert(
      'ÈÄâÊã©ÁÖßÁâá',
      'ËØ∑ÈÄâÊã©Ëé∑ÂèñÁÖßÁâáÁöÑÊñπÂºè',
      [
        { text: 'Áõ∏Êú∫', onPress: () => pickImage('camera') },
        { text: 'Áõ∏ÂÜå', onPress: () => pickImage('library') },
        { text: 'ÂèñÊ∂à', style: 'cancel' }
      ]
    );
  };

  const pickImage = async (source: 'camera' | 'library') => {
    try {
      let result;
      
      if (source === 'camera') {
        // Ê£ÄÊü•Áõ∏Êú∫ÊùÉÈôê
        const cameraStatus = await ImagePicker.getCameraPermissionsAsync();
        if (cameraStatus.status !== 'granted') {
          const requestResult = await ImagePicker.requestCameraPermissionsAsync();
          if (requestResult.status !== 'granted') {
            Alert.alert('ÊùÉÈôê‰∏çË∂≥', 'ÈúÄË¶ÅÁõ∏Êú∫ÊùÉÈôêÊâçËÉΩÊãçÊëÑÁÖßÁâá');
            return;
          }
        }
        
        result = await ImagePicker.launchCameraAsync({
          mediaTypes: ImagePicker.MediaTypeOptions.Images,
          allowsEditing: true,
          aspect: [3, 4],
          quality: 0.8,
        });
      } else {
        // Ê£ÄÊü•Áõ∏ÂÜåÊùÉÈôê
        const mediaLibraryStatus = await ImagePicker.getMediaLibraryPermissionsAsync();
        if (mediaLibraryStatus.status !== 'granted') {
          const requestResult = await ImagePicker.requestMediaLibraryPermissionsAsync();
          if (requestResult.status !== 'granted') {
            Alert.alert('ÊùÉÈôê‰∏çË∂≥', 'ÈúÄË¶ÅÁõ∏ÂÜåÊùÉÈôêÊâçËÉΩÈÄâÊã©ÁÖßÁâá');
            return;
          }
        }
        
        result = await ImagePicker.launchImageLibraryAsync({
          mediaTypes: ImagePicker.MediaTypeOptions.Images,
          allowsEditing: true,
          aspect: [3, 4],
          quality: 0.8,
        });
      }

      if (!result.canceled) {
        const imageUri = result.assets[0].uri;
        handleInputChange('photo_uri', imageUri);
        
        // Ëá™Âä®ÂàÜÊûêÂõæÁâá
        await analyzeImage(imageUri);
      }
    } catch (error) {
      console.error('Error picking image:', error);
      Alert.alert('ÈîôËØØ', 'Ëé∑ÂèñÁÖßÁâáÂ§±Ë¥•ÔºåËØ∑ÈáçËØï');
    }
  };

  const analyzeImage = async (imageUri: string) => {
    setAnalyzing(true);
    setAnalysisProgress(0);
    
    try {
      // Ê®°ÊãüËøõÂ∫¶Êõ¥Êñ∞
      const progressInterval = setInterval(() => {
        setAnalysisProgress(prev => {
          if (prev >= 0.9) {
            clearInterval(progressInterval);
            return prev;
          }
          return prev + 0.1;
        });
      }, 200);

      const analysisResult = await analyzeClothingImage(
        imageUri,
        selectedCategory?.name,
        formData.name
      );

      clearInterval(progressInterval);
      setAnalysisProgress(1);

      if (analysisResult.success && analysisResult.analysis) {
        setSmartTags(analysisResult.analysis);
        
        // Â¶ÇÊûúAIÂàÜÊûêÂá∫‰∫ÜÈ¢úËâ≤ÔºåËá™Âä®Â°´ÂÖÖÂà∞Ë°®Âçï
        if (analysisResult.analysis.colors.length > 0 && !formData.color) {
          handleInputChange('color', analysisResult.analysis.colors[0]);
        }
        
        Alert.alert(
          'ÂàÜÊûêÂÆåÊàê',
          'Â∑≤‰∏∫ÊÇ®ÁöÑË°£Áâ©Ëá™Âä®ÁîüÊàêÊô∫ËÉΩÊ†áÁ≠æÔºåÂåÖÂê´È¢úËâ≤„ÄÅÈ£éÊ†º„ÄÅÈÄÇÂêàÂú∫ÂêàÁ≠â‰ø°ÊÅØ„ÄÇ',
          [{ text: 'Á°ÆÂÆö' }]
        );
      } else {
        // ÂàÜÊûêÂ§±Ë¥•Ôºå‰ΩøÁî®ÈªòËÆ§Ê†áÁ≠æ
        const defaultTags = generateDefaultTags(selectedCategory?.name, formData.name);
        setSmartTags(defaultTags);
        
        Alert.alert(
          'ÂàÜÊûêÂ§±Ë¥•',
          'ÂõæÁâáÂàÜÊûêÂ§±Ë¥•ÔºåÂ∑≤‰∏∫ÊÇ®ÁîüÊàêÂü∫Á°ÄÊ†áÁ≠æ„ÄÇÊÇ®ÂèØ‰ª•ÊâãÂä®ÁºñËæëË°£Áâ©‰ø°ÊÅØ„ÄÇ',
          [{ text: 'Á°ÆÂÆö' }]
        );
      }
    } catch (error) {
      console.error('Error analyzing image:', error);
      const defaultTags = generateDefaultTags(selectedCategory?.name, formData.name);
      setSmartTags(defaultTags);
      
      Alert.alert(
        'ÂàÜÊûêÂ§±Ë¥•',
        'ÂõæÁâáÂàÜÊûêÂ§±Ë¥•ÔºåÂ∑≤‰∏∫ÊÇ®ÁîüÊàêÂü∫Á°ÄÊ†áÁ≠æ„ÄÇÊÇ®ÂèØ‰ª•ÊâãÂä®ÁºñËæëË°£Áâ©‰ø°ÊÅØ„ÄÇ',
        [{ text: 'Á°ÆÂÆö' }]
      );
    } finally {
      setAnalyzing(false);
      setAnalysisProgress(0);
    }
  };

  const validateForm = () => {
    if (!formData.name.trim()) {
      Alert.alert('ÊèêÁ§∫', 'ËØ∑ËæìÂÖ•Ë°£Áâ©ÂêçÁß∞');
      return false;
    }
    
    if (!formData.photo_uri) {
      Alert.alert('ÊèêÁ§∫', 'ËØ∑ÈÄâÊã©ÊàñÊãçÊëÑË°£Áâ©ÁÖßÁâá');
      return false;
    }
    
    if (!selectedCategory) {
      Alert.alert('ÊèêÁ§∫', 'ËØ∑ÈÄâÊã©Ë°£Áâ©ÂàÜÁ±ª');
      return false;
    }
    
    return true;
  };

  const handleSubmit = async () => {
    if (!validateForm() || !selectedCategory) return;
    
    setLoading(true);
    
    try {
      const clothingData = {
        name: formData.name,
        category: selectedCategory.name,
        category_id: parseInt(selectedCategory.id),
        category_name: selectedCategory.name,
        category_color: selectedCategory.color,
        color: formData.color,
        brand: formData.brand,
        size: formData.size,
        location: formData.location,
        notes: formData.notes,
        photo_uri: formData.photo_uri,
        tags: [],
        season: 'ÂÖ®Â≠£',
        isVisible: true,
        wearCount: 0,
        smartTags: smartTags || generateDefaultTags(selectedCategory.name, formData.name)
      };
      
      await addClothing(clothingData);
      
      Alert.alert(
        'ÊàêÂäü',
        'Ë°£Áâ©Ê∑ªÂä†ÊàêÂäüÔºÅ' + (smartTags?.aiAnalyzed ? 'Â∑≤Ëá™Âä®ÁîüÊàêÊô∫ËÉΩÊ†áÁ≠æ„ÄÇ' : ''),
        [{ text: 'Á°ÆÂÆö', onPress: () => navigation.goBack() }]
      );
    } catch (error) {
      console.error('Error adding clothing:', error);
      Alert.alert('ÈîôËØØ', 'Ê∑ªÂä†Ë°£Áâ©Â§±Ë¥•ÔºåËØ∑ÈáçËØï');
    } finally {
      setLoading(false);
    }
  };

  const renderSmartTagsPreview = () => {
    if (!smartTags) return null;

    return (
      <Card style={styles.smartTagsCard}>
        <Card.Content>
          <Title style={styles.sectionTitle}>Êô∫ËÉΩÊ†áÁ≠æ {smartTags.aiAnalyzed ? 'ü§ñ' : 'üìù'}</Title>
          
          {smartTags.colors.length > 0 && (
            <View style={styles.tagSection}>
              <Text style={styles.tagLabel}>È¢úËâ≤:</Text>
              <View style={styles.tagContainer}>
                {smartTags.colors.map((color, index) => (
                  <Chip key={index} style={styles.tag} compact>{color}</Chip>
                ))}
              </View>
            </View>
          )}
          
          {smartTags.styles.length > 0 && (
            <View style={styles.tagSection}>
              <Text style={styles.tagLabel}>È£éÊ†º:</Text>
              <View style={styles.tagContainer}>
                {smartTags.styles.map((style, index) => (
                  <Chip key={index} style={styles.tag} compact>{style}</Chip>
                ))}
              </View>
            </View>
          )}
          
          {smartTags.occasions.length > 0 && (
            <View style={styles.tagSection}>
              <Text style={styles.tagLabel}>ÈÄÇÂêàÂú∫Âêà:</Text>
              <View style={styles.tagContainer}>
                {smartTags.occasions.map((occasion, index) => (
                  <Chip key={index} style={styles.tag} compact>{occasion}</Chip>
                ))}
              </View>
            </View>
          )}
          
          <View style={styles.tagSection}>
            <Text style={styles.tagLabel}>ÈÄÇÂêàÊ∏©Â∫¶:</Text>
            <Text style={styles.tagValue}>
              {smartTags.temperatureRange.min}¬∞C - {smartTags.temperatureRange.max}¬∞C
            </Text>
          </View>
          
          <View style={styles.tagSection}>
            <Text style={styles.tagLabel}>Ê≠£ÂºèÁ®ãÂ∫¶:</Text>
            <Text style={styles.tagValue}>
              {smartTags.formalityLevel}/5 {'‚≠ê'.repeat(smartTags.formalityLevel)}
            </Text>
          </View>
          
          {smartTags.aiAnalyzed && (
            <View style={styles.tagSection}>
              <Text style={styles.tagLabel}>AIÂàÜÊûêÁΩÆ‰ø°Â∫¶:</Text>
              <Text style={styles.tagValue}>
                {Math.round(smartTags.confidence * 100)}%
              </Text>
            </View>
          )}
        </Card.Content>
      </Card>
    );
  };

  return (
    <ScrollView style={styles.container}>
      {/* ÁÖßÁâáÈÄâÊã©Âå∫Âüü */}
      <Card style={styles.photoCard}>
        <TouchableOpacity onPress={handleImagePicker} style={styles.photoContainer}>
          {formData.photo_uri ? (
            <Image source={{ uri: formData.photo_uri }} style={styles.photo} />
          ) : (
            <View style={styles.photoPlaceholder}>
              <Ionicons name="camera" size={48} color={theme.colors.textSecondary} />
              <Text style={styles.photoPlaceholderText}>ÁÇπÂáªÊ∑ªÂä†ÁÖßÁâá</Text>
            </View>
          )}
        </TouchableOpacity>
        
        {/* ÂàÜÊûêËøõÂ∫¶ */}
        {analyzing && (
          <View style={styles.analysisContainer}>
            <View style={styles.analysisHeader}>
              <ActivityIndicator size="small" color={theme.colors.primary} />
              <Text style={styles.analysisText}>Ê≠£Âú®ÂàÜÊûêÂõæÁâá...</Text>
            </View>
            <ProgressBar progress={analysisProgress} color={theme.colors.primary} />
          </View>
        )}
      </Card>

      {/* Êô∫ËÉΩÊ†áÁ≠æÈ¢ÑËßà */}
      {renderSmartTagsPreview()}

      {/* Âü∫Êú¨‰ø°ÊÅØ */}
      <Card style={styles.formCard}>
        <Card.Content>
          <Title style={styles.sectionTitle}>Âü∫Êú¨‰ø°ÊÅØ</Title>
          
          <TextInput
            label="Ë°£Áâ©ÂêçÁß∞ *"
            value={formData.name}
            onChangeText={(text) => handleInputChange('name', text)}
            style={styles.input}
            mode="outlined"
          />

          <Menu
            visible={categoryMenuVisible}
            onDismiss={() => setCategoryMenuVisible(false)}
            anchor={
              <TouchableOpacity
                onPress={() => setCategoryMenuVisible(true)}
                style={styles.categorySelector}
              >
                <Text style={styles.categoryLabel}>ÂàÜÁ±ª *</Text>
                <View style={styles.categoryValue}>
                  {selectedCategory ? (
                    <Chip>{selectedCategory.name}</Chip>
                  ) : (
                    <Text style={styles.categoryPlaceholder}>ËØ∑ÈÄâÊã©ÂàÜÁ±ª</Text>
                  )}
                  <Ionicons name="chevron-down" size={20} color={theme.colors.textSecondary} />
                </View>
              </TouchableOpacity>
            }
          >
            {categories.map((category) => (
              <Menu.Item
                key={category.id}
                onPress={() => handleCategorySelect(category)}
                title={category.name}
              />
            ))}
          </Menu>

          <TextInput
            label="ÂìÅÁâå"
            value={formData.brand}
            onChangeText={(text) => handleInputChange('brand', text)}
            style={styles.input}
            mode="outlined"
          />

          <View style={styles.row}>
            <TextInput
              label="È¢úËâ≤"
              value={formData.color}
              onChangeText={(text) => handleInputChange('color', text)}
              style={[styles.input, styles.halfInput]}
              mode="outlined"
            />
            <TextInput
              label="Â∞∫Á†Å"
              value={formData.size}
              onChangeText={(text) => handleInputChange('size', text)}
              style={[styles.input, styles.halfInput]}
              mode="outlined"
            />
          </View>

          <TextInput
            label="Â≠òÊîæ‰ΩçÁΩÆ"
            value={formData.location}
            onChangeText={(text) => handleInputChange('location', text)}
            style={styles.input}
            mode="outlined"
            placeholder="Â¶ÇÔºö‰∏ªÂçßË°£Êüú„ÄÅÊ¨°ÂçßÊäΩÂ±âÁ≠â"
          />

          <TextInput
            label="Â§áÊ≥®"
            value={formData.notes}
            onChangeText={(text) => handleInputChange('notes', text)}
            style={styles.input}
            mode="outlined"
            multiline
            numberOfLines={3}
          />
        </Card.Content>
      </Card>

      {/* Êèê‰∫§ÊåâÈíÆ */}
      <View style={styles.submitContainer}>
        <Button
          mode="contained"
          onPress={handleSubmit}
          loading={loading}
          disabled={loading || analyzing}
          style={styles.submitButton}
        >
          {analyzing ? 'Ê≠£Âú®ÂàÜÊûê...' : 'Ê∑ªÂä†Ë°£Áâ©'}
        </Button>
      </View>
    </ScrollView>
  );
};

